const API_KEY = process.env.REACT_APP_ES_API_KEY;

const formatTransaction = (transactions: Array<any>): Array<any> =>
    transactions.map((transaction) => {
        return {
            hash: transaction.hash,
            link: `https://rinkeby.etherscan.io/tx/${transaction.hash}`,
        };
    });
export const getTransactions = async (address: string, net: string) => {
    try {
        const url = `https://api${
            net === "rinkeby" ? "-rinkeby" : ""
        }.etherscan.io/api?module=account&action=txlist&startblock=999999&endblock=99999999&page=1&offset=10&sort=asc&apikey=${API_KEY}&address=${address}`;
        const query = await fetch(url);
        const json = await query.json();
        if (json.message === "OK") {
            return net === "rinkeby"
                ? formatTransaction(json.result)
                : json.result;
        }
        return [];
    } catch (e) {
        console.error(e);
    }
};
